#include<iostream>
using namespace std;

int main(){
    int num1, num2;
    num1 = 8;
    num2 = 15;


    cout << "8 == 15 is " << (num1 == num2) << endl;
    cout << "8 != 15 is " << (num1 != num2) << endl;
    cout << "8 > 15 is " << (num1 > num2) << endl;
    cout << "8 < 15 is " << (num1 < num2) << endl;
    cout << "8 >= 15 is " << (num1 >= num2) << endl;
    cout << "8 <= 15 is " << (num1 <= num2) << endl;

    return 0;
}
